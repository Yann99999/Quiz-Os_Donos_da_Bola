var pt =100;
function pergunta1() {
    let alt11 = document.getElementById("alt11").checked
    let resultado = "Você é um monstro sagrado, garotinho"
    let resultado1 = "Você é um baita dum pé de rato, seu Oreiudo"

    if (alt11 == true) {
        document.getElementById("resp1").textContent = resultado
        document.getElementById("result").textContent = pt
        pt = pt + 100
    }
    else{
        document.getElementById("resp1").textContent = resultado1
    }
}
